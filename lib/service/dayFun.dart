
String  theDayIs(int dayofweak){
  switch(dayofweak){

    case 7 :
      return 'Sun';

    case 1 :
      return 'Mon' ;

    case 2 :
     return 'Tus';
    case 3 :
      return 'Wed';
    case 4 :
      return 'Thu';
    case 5 :
      return 'Fri';
    case 6 :
      return 'Sat';
    default:
      return '';
  }
}
